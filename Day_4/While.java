public class While {
      public static void main(String[] args){
            int i = 'A';
            while (i < 188) {
                  System.out.println((char)i);
                  i++;
            }

            int countdown = 3;

            while (countdown > 0) {
                  System.out.println(countdown);
                  countdown--;
            }

            System.out.println("Happy New Year!!");
      }
}
